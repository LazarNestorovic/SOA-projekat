const fs = require('fs');
const path = require('path');
const root = path.join(__dirname, '..', 'apps', 'web', 'node_modules');
if (!fs.existsSync(root)) {
	console.log('no node_modules');
	process.exit(0);
}
const bad = [];
function walk(dir) {
	const items = fs.readdirSync(dir, { withFileTypes: true });
	for (const it of items) {
		const p = path.join(dir, it.name);
		if (it.isDirectory()) {
			if (it.name.startsWith('@')) {
				// scope
				walk(p);
			} else {
				const pkg = path.join(p, 'package.json');
				if (fs.existsSync(pkg)) {
					try {
						const j = JSON.parse(fs.readFileSync(pkg, 'utf8'));
						const v = j.version;
						if (!v || String(v).trim() === '') bad.push(pkg);
					} catch (e) {
						bad.push(pkg + ' (parse error)');
					}
				}
				// don't recurse into every nested node_modules to save time
			}
		}
	}
}
walk(root);
if (bad.length) {
	console.log('Found bad package.json versions:', bad.slice(0, 50));
	process.exit(1);
} else {
	console.log('No bad package.json versions found under node_modules');
}
